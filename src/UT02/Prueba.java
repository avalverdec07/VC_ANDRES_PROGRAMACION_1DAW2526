package UT02;

public class Prueba {
	String hola;
	String adios;
	int coches;
	double dinero;
	
	
	public String getHola() {
		return hola;
	}
	public void setHola(String hola) {
		this.hola = hola;
	}
	public String getAdios() {
		return adios;
	}
	public void setAdios(String adios) {
		this.adios = adios;
	}
	public int getCoches() {
		return coches;
	}
	public void setCoches(int coches) {
		this.coches = coches;
	}
	public double getDinero() {
		return dinero;
	}
	public void setDinero(double dinero) {
		this.dinero = dinero;
	}
	
	
	
	
}
